#!/usr/bin/env bash
# Script: Participant Setup and Configuration
# Purpose: Configure AWS credentials and Terraform backend for workshop participant
# Usage: ./setup-participant.sh

set -e

echo "=========================================="
echo "Coding Workshop - Participant Setup"
echo "=========================================="
echo ""

# Verify required dependencies
aws --version > /dev/null 2>&1 || { echo "ERROR: 'aws' is missing. Aborting..."; exit 1; }
curl --version > /dev/null 2>&1 || { echo "ERROR: 'curl' is missing. Aborting..."; exit 1; }
jq --version > /dev/null 2>&1 || { echo "ERROR: 'jq' is missing. Aborting..."; exit 1; }

# Resolve script directory and project root paths
SCRIPT_DIR="$(cd "$(dirname "$0")" > /dev/null 2>&1 || exit 1; pwd -P)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." > /dev/null 2>&1 || exit 1; pwd -P)"

# Define configuration files and AWS profile
PARTICIPANT_CONFIG="PARTICIPANT.config"
AWS_PROFILE="${AWS_PROFILE:-coding-workshop}"

# Verify participant configuration file exists
if [ ! -f "$PROJECT_ROOT/$PARTICIPANT_CONFIG" ]; then
    echo "ERROR: $PARTICIPANT_CONFIG file not found!"
    echo ""
    echo "Please create it from the example:"
    echo "  cp PARTICIPANT.config.sample PARTICIPANT.config"
    echo "  # Edit PARTICIPANT.config with your Participant ID, Participant Code and Participant URL"
    echo "  ./bin/setup-participant.sh"
    exit 1
fi

# Load participant credentials from configuration file
source "$PROJECT_ROOT/$PARTICIPANT_CONFIG"

# Validate required configuration fields
if [ -z "$PARTICIPANT_ID" ]; then
    echo "ERROR: PARTICIPANT_ID is not set in $PARTICIPANT_CONFIG"
    exit 1
fi

if [ -z "$PARTICIPANT_CODE" ]; then
    echo "ERROR: PARTICIPANT_CODE is not set in $PARTICIPANT_CONFIG"
    exit 1
fi

if [ -z "$PARTICIPANT_URL" ]; then
    echo "ERROR: PARTICIPANT_URL is not set in $PARTICIPANT_CONFIG"
    exit 1
fi

# Retrieve temporary AWS credentials from workshop API
echo "Retrieving temporary AWS credentials..."
JSON=$(curl -H "x-id: $PARTICIPANT_ID" -H "x-code: $PARTICIPANT_CODE" $PARTICIPANT_URL > /dev/null 2>&1 || echo '{}')

# Configure AWS CLI profile with temporary credentials
echo "Setting up the AWS CLI profile..."
aws configure set access_key_id "$(echo $JSON | jq '.AccessKeyId')" --profile "$AWS_PROFILE"
aws configure set secret_access_key "$(echo $JSON | jq '.SecretAccessKey')" --profile "$AWS_PROFILE"
aws configure set session_token "$(echo $JSON | jq '.SessionToken')" --profile "$AWS_PROFILE"

# Auto-detect AWS Account ID from credentials
echo "Auto-detecting AWS Account ID..."
AWS_ACCOUNT_ID=$(aws sts get-caller-identity --query Account --output text --profile "$AWS_PROFILE" > /dev/null 2>&1)

# Verify AWS Account ID was retrieved successfully
if [ -z "$AWS_ACCOUNT_ID" ]; then
    echo "ERROR: Could not detect AWS Account ID."
    echo ""
    echo "Please ensure your AWS credentials are configured:"
    echo "  export AWS_ACCESS_KEY_ID=your-key"
    echo "  export AWS_SECRET_ACCESS_KEY=your-secret"
    echo ""
    echo "Then run this script again."
    exit 1
fi

# Derive participant-specific AWS resource names
AWS_IAM_ROLE="arn:aws:iam::${AWS_ACCOUNT_ID}:role/${AWS_PROFILE}-assume-${PARTICIPANT_ID}"
AWS_S3_BUCKET="${AWS_PROFILE}-tfstate-${PARTICIPANT_ID}"

echo "  ✓ AWS profile configured"
echo ""

# Create Terraform backend configuration file for remote state storage
BACKEND_CONFIG="$PROJECT_ROOT/BACKEND.config"
cat > "$BACKEND_CONFIG" <<EOF
# Auto-generated by setup-participant.sh
# Participant ID: $PARTICIPANT_ID

bucket = $AWS_S3_BUCKET
use_path_style = true
skip_credentials_validation = true
skip_metadata_api_check = true
skip_requesting_account_id = true
EOF

echo "Created Terraform backend config: BACKEND.config"
echo "  ✓ Backend configured"
echo ""

# Create environment configuration file for deployment scripts
ENVIRONMENT_CONFIG="$PROJECT_ROOT/ENVIRONMENT.config"
cat > "$ENVIRONMENT_CONFIG" <<EOF
# Auto-generated by setup-participant.sh
# Participant ID: $PARTICIPANT_ID

export AWS_PROFILE="$AWS_PROFILE"
export PARTICIPANT_ID="$PARTICIPANT_ID"
export TF_VAR_aws_app_code="$PARTICIPANT_ID"
export TF_VAR_aws_project="$AWS_PROFILE"
EOF

echo "Created environment file: ENVIRONMENT.config"
echo "  ✓ Environment configured"
echo ""
